// 解析开奖结果字符串
export const parseResult = (result: string) => {
  const [frontStr, backStr] = result.split('|');
  const front = frontStr.split('#').map(num => parseInt(num, 10));
  const back = backStr ? backStr.split('#').map(num => parseInt(num, 10)) : [];
  return { front, back };
};

